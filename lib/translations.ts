export function t(key: string, locale: string = 'ar'): string {
  const keys = key.split('.');
  let value: any = translations[locale as 'ar' | 'en'];
  
  for (const k of keys) {
    value = value?.[k];
    if (!value) break;
  }
  
  return value || key;
}

